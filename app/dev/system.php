<div class="txt-img">

	<h2>Was ist <span class="caps">REDAXO</span></h2>

	<p><span class="caps">REDAXO</span> ist ein Content Management System für individuelle, vielfältige und flexible Web-Lösungen.</p>

	<h2>Merkmale:</h2>

	<ul>
		<li>Trennung von Inhalt und Layout mittels Templates</li>
		<li>Die Verwaltung von mehrsprachigen Webseiten ist gegeben</li>
		<li>Der Inhalt setzt sich aus verschiedenen Modulen zusammen</li>
		<li>Keine Grenzen bei der Erstellung von Modulen</li>
		<li>Systemunabhängiges sowie plattformübergreifendes Arbeiten über den Webbrowser</li>
		<li>Linkmanagement</li>
		<li>Keine Einschränkungen bei der Entwicklung von barrierefreiem Webdesign</li>
		<li>Aufnahme von Metadaten für Suchmaschinen möglich</li>
		<li>Suchfunktionen können integriert werden</li>
		<li>Rechteverteilung sind möglich</li>
		<li>Medienverwaltung über Medienpool (<span class="caps">HTML</span>, <span class="caps">XML</span>, <span class="caps">PDF</span>, MP3, <span class="caps">DOC</span>, <span class="caps">JPEG</span>, <span class="caps">GIF</span> etc.)</li>
		<li>Import / Export Funktion ermöglicht Projektsicherung</li>
		<li>Einbindung von Erweiterungen/Addons für unterschiedlichste Funktionen, auf der <span class="caps">REDAXO</span>-Website gibt es zahlreiche Addons zum Download</li>
		<li><span class="caps">REDAXO</span> passt sich dem eigenen Wissensstand an</li>
		<li><span class="caps">REDAXO</span> basiert auf <span class="caps">PHP</span> / MySQL</li>
	</ul>

</div>


